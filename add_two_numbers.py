class mathematics():
    def __init__(self):
        pass

    def addition(self, a, b, c):
        d = a + b + c
        return d
    
    def multiplication(self, a, b, c):
        e = a * b * c
        return e

res = mathematics()
result = res.addition(5, 6, 7)
product = res.multiplication(8, 6, 7)

print(result)
print(product)